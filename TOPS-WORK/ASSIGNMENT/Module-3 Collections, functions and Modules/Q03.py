"""
Differentiate between append () and extend () methods?

ans: 
append()
Adds a single element to the end of the list.

exa: 
list = [1, 2, 3]
list.append(4)
#output: [1, 2, 3, 4]

extend()
Adds multiple elements (from an iterable) to the end of the list.

exa:
list = [1, 2, 3]
list.extend([4, 5])
#output: [1, 2, 3, 4, 5]
"""